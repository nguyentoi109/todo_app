              const Text(
                'Todo App',
                style: TextStyle(
                  color: Colors.white,
                  fontSize: 26,
                  fontWeight: FontWeight.w800,
                ),
              ),
              const SizedBox(height: 6),
              Text(
                'Quản lý công việc thông minh',
                style: TextStyle(
                  color: Colors.white.withOpacity(0.85),
